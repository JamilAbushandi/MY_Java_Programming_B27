package day05_arithmetics_operators;

public class MathPractice {
    public static void main(String[] args) {

        System.out.println(2*5); // int * int ---> int
        System.out.println(2*5.5); // int * double ---> double, so 11.0
        System.out.println(10/3); // int / int ---> int, so answer will only be 3
        System.out.println(10/3.0); // int / double ---> double, so answer will be 3.3333
        System.out.println(5%2); // int % int ---> int, so answer is 1
        System.out.println(4%2); // same answer will be 0 coz nothing remaining after the calculation
        System.out.println(4/10); // can't divide so answer will remain the 4 itself



    }
}
